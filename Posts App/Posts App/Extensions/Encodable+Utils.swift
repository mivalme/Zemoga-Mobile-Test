import Foundation

extension Encodable {
    func dictionary() -> [String: Any]? {
        let encoder = JSONEncoder()
        guard let json = try? encoder.encode(self),
            let dict = try? JSONSerialization.jsonObject(with: json, options: []) as? [String: Any] else {
                return nil
        }
        return dict
    }
}
